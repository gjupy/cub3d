#include "libft.h"

char	*ft_strchr(const char *s, int ch)
{
	char	c;

	c = ch;
	while (*s != '\0')
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
